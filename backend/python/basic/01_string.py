#!/usr/bin/env python3

# 字符串基础操作
str = 'Runoob'

print(str)              # 输出字符串
print(str[0])           # 输出第一个字符
print(str[1:-2])        # 输出第一个 到 倒数第二个 的所有字符
print(str[2:])          # 输出第二个开始的后面的所有字符
print(str * 2)          # 输出字符串两次
print(str + '你好')      # 连接字符串
print('hello\nJay')     # 使用反斜杠转义字符
print(r'hello\nJay')    # 使反斜杠无法转义